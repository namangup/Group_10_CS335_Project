//Identifiers
int main(){
    int a1_;
    int _a1;
    int a_1;
    int A1_;
    a1_=5;
    _a1=6;
    a_1=7;
    A1_=8;
    printf("a1_=%d\n",a1_);
    printf("_a1=%d\n",_a1);
    printf("a_1=%d\n",a_1);
    printf("A1_=%d\n",A1_);
    return 0;
}

